function startTypewriter(lang){
  clearInterval(typewriterTimer);
  clearTimeout(typewriterTimer);
  const overlay = document.getElementById('typewriterOverlay');
  const askInput = document.getElementById('askInput');
  if(!overlay || !askInput) return;
  const bank = typewriterSamples[lang] || typewriterSamples.en;
  const samples = bank[currentTopic] || bank.disease;

  let sampleIndex = 0, charIndex = 0, deleting = false;

  function tick(){
    const full = samples[sampleIndex];
    if(!deleting){
      charIndex++;
      overlay.innerHTML = full.slice(0, charIndex) + '<span class="ask-cursor-blink"></span>';
      if(charIndex >= full.length){
        deleting = true;
        typewriterTimer = setTimeout(tick, 1600);
        return;
      }
      typewriterTimer = setTimeout(tick, 45);
    } else {
      charIndex--;
      overlay.innerHTML = full.slice(0, charIndex) + '<span class="ask-cursor-blink"></span>';
      if(charIndex <= 0){
        deleting = false;
        sampleIndex = (sampleIndex + 1) % samples.length;
        typewriterTimer = setTimeout(tick, 300);
        return;
      }
      typewriterTimer = setTimeout(tick, 25);
    }
  }
  overlay.innerHTML = '<span class="ask-cursor-blink"></span>';
  typewriterTimer = setTimeout(tick, 400);
}

function goldify(text){
  return text
    .replace(/\*\*(.+?)\*\*/g, '<span class="accent-gold">$1</span>')
    .replace(/\bAI\b/g, '<span class="accent-ai">AI</span>');
}

function applyLanguage(lang){
  const dict = translations[lang] || translations.en;
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const key = el.getAttribute('data-i18n');
    if(dict[key]) el.innerHTML = goldify(dict[key]);
  });
  document.documentElement.setAttribute('lang', lang);
  document.documentElement.setAttribute('data-lang', lang);
  document.getElementById('langLabel').textContent = langNames[lang];
  document.querySelectorAll('.lang-menu li').forEach(li=>{
    li.classList.toggle('active', li.getAttribute('data-lang') === lang);
  });
  localStorage.setItem('kisanLang', lang);
  startTypewriter(lang);
}

